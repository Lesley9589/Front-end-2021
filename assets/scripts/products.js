function addCart() {
  let doc1 = document.getElementById("car_name");
  let doc2 = document.getElementById("price");

  fetch("https://shrouded-brook-12387.herokuapp.com/add-car/", {
    method: "POST",
    body: JSON.stringify({
      car_name: doc1.innerHTML,
      price: doc2.innerHTML,
    }),
    headers: {
      "Content-type": "application/json; chartset=UTF-8",
    },
  })
    .then((response) => response.json())
    .then((json) => {
      alert("Item Added To Cart");
      console.log(json);
    });
}

